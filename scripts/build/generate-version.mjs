#!/usr/bin/env node
// Generates src/lib/version.ts with the current git SHA + build timestamp.
// Runs as a prebuild step. Must NEVER fail the build — falls back to 'unknown'.
import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..', '..');
const outPath = resolve(repoRoot, 'src', 'lib', 'version.ts');

function readSha() {
  try {
    return execSync('git rev-parse HEAD', { cwd: repoRoot, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  } catch {
    return 'unknown';
  }
}

const sha = readSha();
const shaShort = sha === 'unknown' ? 'unknown' : sha.slice(0, 7);
const buildTime = new Date().toISOString();

const content = `// Generated by scripts/build/generate-version.mjs — do not edit.
// Regenerated on every pnpm dev / pnpm build via the gen:version prebuild step.
export const CURRENT_SHA = ${JSON.stringify(sha)};
export const CURRENT_SHA_SHORT = ${JSON.stringify(shaShort)};
export const BUILD_TIME = ${JSON.stringify(buildTime)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, content, 'utf8');
console.log(`[gen:version] wrote ${outPath} (sha=${shaShort}, buildTime=${buildTime})`);
